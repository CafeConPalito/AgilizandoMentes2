package tablaalumno;

/**
 *
 * @author terciodemarte
 */
public class ObjetoAlumnoBBDD {
    private String usuario,alias,nombre,apellido1,apellido2,dni,email,fecha,curso;

    public ObjetoAlumnoBBDD(String usuario, String alias, String nombre, String apellido1, String apellido2, String dni, String email, String fecha, String curso) {
        this.usuario = usuario;
        this.alias = alias;
        this.nombre = nombre;
        this.apellido1 = apellido1;
        this.apellido2 = apellido2;
        this.dni = dni;
        this.email = email;
        this.fecha = fecha;
        this.curso = curso;
    }

    public ObjetoAlumnoBBDD() {
    }
    

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido1() {
        return apellido1;
    }

    public void setApellido1(String apellido1) {
        this.apellido1 = apellido1;
    }

    public String getApellido2() {
        return apellido2;
    }

    public void setApellido2(String apellido2) {
        this.apellido2 = apellido2;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    @Override
    public String toString() {
        return usuario+alias+nombre+apellido1+apellido2+dni+email+fecha+curso;
    }
    
    
    
    
}
